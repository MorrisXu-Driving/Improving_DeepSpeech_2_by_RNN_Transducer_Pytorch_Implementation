def main(learning_rate=5e-4, batch_size=32, epochs=10,
         train_url="train-clean-100", test_url="test-clean",
         experiment=Experiment(api_key='dummy_key', disabled=True)):
    hparams = {
        # Encoder Parameters
        "n_cnn_layers": 3,
        "n_rnn_layers": 5,
        "rnn_dim": 512,
        "n_class": 29,
        "n_feats": 128,
        "stride": 2,
        "dropout": 0.2,
        "learning_rate": learning_rate,
        "batch_size": batch_size,
        # Decoder Parameters
        "hidden_size": 256,
        "vocab_size": 29,
        "output_size": 29,
        "n_layers": 1,
        # Joint Parameters,
        "input_size": 58,
        "inner_dim": 256,
        "epochs": epochs,
    }
    experiment.log_parameters(hparams)

    use_cuda = torch.cuda.is_available()
    torch.manual_seed(7)
    device = torch.device("cuda" if use_cuda else "cpu")

    if not os.path.isdir("./data"):
        os.makedirs("./data")

    train_dataset = torchaudio.datasets.LIBRISPEECH("./data", url=train_url, download=True)
    test_dataset = torchaudio.datasets.LIBRISPEECH("./data", url=test_url, download=True)

    kwargs = {'num_workers': 1, 'pin_memory': True} if use_cuda else {}

    train_loader = data.DataLoader(dataset=train_dataset,
                                   batch_size=hparams['batch_size'],
                                   shuffle=True,
                                   collate_fn=lambda x: data_processing(x, 'train'),
                                   **kwargs)
    test_loader = data.DataLoader(dataset=test_dataset,
                                  batch_size=hparams['batch_size'],
                                  shuffle=False,
                                  collate_fn=lambda x: data_processing(x, 'valid'),
                                  **kwargs)

    model = Transducer(
        hparams['n_cnn_layers'], hparams['n_rnn_layers'], hparams['rnn_dim'],
        hparams['n_class'], hparams['n_feats'], hparams['hidden_size'], hparams['vocab_size'],
        hparams['output_size'], hparams['input_size'], hparams['inner_dim'], hparams['n_layers']
    ).to(device)

    # Model Parameter Initialization
    def init_weights(m):
        if type(m) == nn.Linear or type(m) == nn.Conv2d:
            torch.nn.init.xavier_uniform_(m.weight)
        else:
            if type(m) == nn.Embedding:
                torch.nn.init.uniform_(m.weight)
            else:
                if type(m) == nn.GRU or type(m) == nn.LSTM:
                    torch.nn.init.orthogonal_(m.weight_ih_l0)
                    torch.nn.init.orthogonal_(m.weight_hh_l0)

    model.apply(init_weights)  # Initialize before start of training
    #     model.load_state_dict(torch.load("rnnt.params"))
    print(model)
    print('Num Model Parameters', sum([param.nelement() for param in model.parameters()]))

    optimizer = optim.AdamW(model.parameters(), hparams['learning_rate'])
    criterion = RNNTLoss()
    scheduler = optim.lr_scheduler.OneCycleLR(optimizer, max_lr=hparams['learning_rate'],
                                              steps_per_epoch=int(len(train_loader)),
                                              epochs=hparams['epochs'],
                                              anneal_strategy='linear')

    iter_meter = IterMeter()
    for epoch in range(1, epochs + 1):
        train(model, device, train_loader, criterion, optimizer, scheduler, epoch, iter_meter, experiment)
        torch.save(model.state_dict(), 'rnnt.params')  # Save trained model after one-epoch training
        test(model, device, test_loader, criterion, epoch, iter_meter, experiment)


